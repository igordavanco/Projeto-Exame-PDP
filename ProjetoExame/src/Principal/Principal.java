    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) tbl_produtos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = factory.getProdutoDAO();
        
        for(Produto p: pdao.read()){
            
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getCategoria_id(),
                p.getFornecedor_id()
            });
            
        }
    }
    
    public void readJTableCat(){
        DefaultTableModel categoria = (DefaultTableModel) tbl_categorias.getModel();
        categoria.setNumRows(0);
        CategoriaDAO cdao = factory.getCategoriaDAO();
        
        for(Categoria c: cdao.read()){
            
            categoria.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getDescricao()
            });
            
        }
    }
    
    public void readJTableFor(){
        DefaultTableModel fornecedor = (DefaultTableModel) tbl_fornecedores.getModel();
        fornecedor.setNumRows(0);
        FornecedorDAO fdao = factory.getFornecedorDAO();
        
        for(Fornecedor f: fdao.read()){
            
            fornecedor.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getEndereco(),
                f.getCidade(),
                f.getTelefone()
            });
            
        }
    }

