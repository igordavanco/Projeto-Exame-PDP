    private void btn_for_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_for_excluirActionPerformed
        if(tbl_fornecedores.getSelectedRow() != -1){
            Fornecedor f = new Fornecedor();
            FornecedorDAO fdao = factory.getFornecedorDAO();

            f.setId((int)tbl_fornecedores.getValueAt(tbl_fornecedores.getSelectedRow(), 0));

            fdao.delete(f);

            c_for_nome.setText("");
            c_for_endereco.setText("");
            c_for_cidade.setText("");
            c_for_telefone.setText("");

            readJTableFor();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um fornecedor para ser excluído!");
        }

        c_for_nome.setEnabled(false);
        c_for_endereco.setEnabled(false);
        c_for_cidade.setEnabled(false);
        c_for_telefone.setEnabled(false);
        btn_for_salvar.setEnabled(false);
        btn_for_cancelar.setEnabled(false);
        btn_for_editar.setEnabled(false);
        btn_for_excluir.setEnabled(false);
        btn_for_novo.setEnabled(true);
    }//GEN-LAST:event_btn_for_excluirActionPerformed

